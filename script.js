const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;

function initHeroMotion() {
  const canvas = document.querySelector(".hero-motion");
  const hero = document.querySelector(".hero");
  if (!canvas || !hero || reduceMotion) return;

  const context = canvas.getContext("2d");
  const pointer = { x: 0.72, y: 0.36 };
  let width = 0;
  let height = 0;
  let frame = 0;
  const particles = Array.from({ length: 54 }, (_, index) => ({
    seed: index * 19.17,
    x: Math.random(),
    y: Math.random(),
    radius: 1.5 + Math.random() * 4.5,
    speed: 0.18 + Math.random() * 0.5,
    drift: Math.random() * 0.8 + 0.2,
    alpha: 0.18 + Math.random() * 0.38,
  }));

  function resize() {
    const rect = hero.getBoundingClientRect();
    const ratio = Math.min(window.devicePixelRatio || 1, 2);
    width = Math.max(1, Math.floor(rect.width));
    height = Math.max(1, Math.floor(rect.height));
    canvas.width = Math.floor(width * ratio);
    canvas.height = Math.floor(height * ratio);
    canvas.style.width = `${width}px`;
    canvas.style.height = `${height}px`;
    context.setTransform(ratio, 0, 0, ratio, 0, 0);
  }

  function draw() {
    frame += 0.012;
    context.clearRect(0, 0, width, height);

    const glow = context.createRadialGradient(
      pointer.x * width,
      pointer.y * height,
      0,
      pointer.x * width,
      pointer.y * height,
      width * 0.42
    );
    glow.addColorStop(0, "rgba(244, 197, 66, 0.14)");
    glow.addColorStop(0.45, "rgba(217, 31, 38, 0.08)");
    glow.addColorStop(1, "rgba(255, 255, 255, 0)");
    context.fillStyle = glow;
    context.fillRect(0, 0, width, height);

    particles.forEach((particle) => {
      const wave = Math.sin(frame * particle.speed + particle.seed);
      const x = (particle.x * width + wave * 34 * particle.drift + pointer.x * 18) % width;
      const y = (particle.y * height + frame * 60 * particle.speed + Math.cos(frame + particle.seed) * 18) % height;

      context.beginPath();
      context.arc(x, y, particle.radius, 0, Math.PI * 2);
      context.fillStyle = `rgba(255, 255, 255, ${particle.alpha})`;
      context.fill();

      context.beginPath();
      context.arc(x, y, particle.radius * 2.6, 0, Math.PI * 2);
      context.strokeStyle = `rgba(244, 197, 66, ${particle.alpha * 0.16})`;
      context.lineWidth = 1;
      context.stroke();
    });

    requestAnimationFrame(draw);
  }

  hero.addEventListener("pointermove", (event) => {
    const rect = hero.getBoundingClientRect();
    pointer.x = (event.clientX - rect.left) / rect.width;
    pointer.y = (event.clientY - rect.top) / rect.height;
  });

  resize();
  window.addEventListener("resize", resize);
  requestAnimationFrame(draw);
}

function initParticleTextBackground() {
  const canvas = document.querySelector(".particle-text-bg");
  const section = document.querySelector(".intro-section");
  if (!canvas || !section || reduceMotion) return;

  const context = canvas.getContext("2d");
  const measureCanvas = document.createElement("canvas");
  const measureContext = measureCanvas.getContext("2d");
  const state = {
    width: 1,
    height: 1,
    ratio: 1,
    wordIndex: 0,
    lastSwitch: 0,
    words: [],
    pointer: { x: -9999, y: -9999 },
  };

  const particles = Array.from({ length: 620 }, () => ({
    x: Math.random() * 800,
    y: Math.random() * 360,
    tx: Math.random() * 800,
    ty: Math.random() * 360,
    vx: 0,
    vy: 0,
    size: 1.1 + Math.random() * 1.9,
    orbit: Math.random() * Math.PI * 2,
    color: Math.random() > 0.74 ? "244, 197, 66" : Math.random() > 0.45 ? "255, 255, 255" : "217, 31, 38",
  }));

  function getWords() {
    const language = document.documentElement.lang === "ru" ? "ru" : "uk";
    const raw = language === "ru" ? canvas.dataset.wordsRu : canvas.dataset.wordsUk;
    return (raw || "EXTREME|SELF WASH").split("|").filter(Boolean);
  }

  function resize() {
    const rect = section.getBoundingClientRect();
    state.ratio = Math.min(window.devicePixelRatio || 1, 2);
    state.width = Math.max(1, Math.floor(rect.width));
    state.height = Math.max(1, Math.floor(rect.height));
    canvas.width = Math.floor(state.width * state.ratio);
    canvas.height = Math.floor(state.height * state.ratio);
    canvas.style.width = `${state.width}px`;
    canvas.style.height = `${state.height}px`;
    context.setTransform(state.ratio, 0, 0, state.ratio, 0, 0);

    measureCanvas.width = state.width;
    measureCanvas.height = state.height;
    assignWordTargets(true);
  }

  function assignWordTargets(force = false) {
    state.words = getWords();
    if (!state.words.length) return;
    if (force) state.wordIndex = state.wordIndex % state.words.length;

    const word = state.words[state.wordIndex % state.words.length];
    measureContext.clearRect(0, 0, state.width, state.height);
    measureContext.fillStyle = "#fff";
    measureContext.textAlign = "center";
    measureContext.textBaseline = "middle";
    const fontSize = Math.min(state.width / Math.max(word.length * 0.46, 4.2), state.height * 0.22, 144);
    const targetY = state.width < 560 ? 190 : 170;
    measureContext.font = `900 ${Math.max(42, fontSize)}px Oswald, Impact, sans-serif`;
    measureContext.fillText(word, state.width * 0.66, targetY);

    const image = measureContext.getImageData(0, 0, state.width, state.height).data;
    const points = [];
    const step = state.width < 560 ? 5 : 6;

    for (let y = 0; y < state.height; y += step) {
      for (let x = 0; x < state.width; x += step) {
        const alpha = image[(y * state.width + x) * 4 + 3];
        if (alpha > 40) points.push({ x, y });
      }
    }

    particles.forEach((particle, index) => {
      const point = points[index % Math.max(points.length, 1)];
      const fallbackX = state.width * (0.12 + Math.random() * 0.76);
      const fallbackY = state.height * (0.16 + Math.random() * 0.68);
      particle.tx = point ? point.x + (Math.random() - 0.5) * 2 : fallbackX;
      particle.ty = point ? point.y + (Math.random() - 0.5) * 2 : fallbackY;
    });
  }

  function draw(timestamp = 0) {
    if (!state.lastSwitch) state.lastSwitch = timestamp;
    if (timestamp - state.lastSwitch > 3200) {
      state.lastSwitch = timestamp;
      state.wordIndex = (state.wordIndex + 1) % Math.max(state.words.length, 1);
      assignWordTargets();
    }

    context.clearRect(0, 0, state.width, state.height);
    context.fillStyle = "rgba(11, 17, 24, 0.08)";
    context.fillRect(0, 0, state.width, state.height);

    particles.forEach((particle) => {
      const dx = particle.tx - particle.x;
      const dy = particle.ty - particle.y;
      const pointerDx = particle.x - state.pointer.x;
      const pointerDy = particle.y - state.pointer.y;
      const pointerDistance = Math.hypot(pointerDx, pointerDy);
      let pushX = 0;
      let pushY = 0;

      if (pointerDistance < 120) {
        const force = (120 - pointerDistance) / 120;
        pushX = (pointerDx / Math.max(pointerDistance, 1)) * force * 4.2;
        pushY = (pointerDy / Math.max(pointerDistance, 1)) * force * 4.2;
      }

      particle.orbit += 0.02;
      particle.vx += dx * 0.028 + pushX + Math.cos(particle.orbit) * 0.012;
      particle.vy += dy * 0.028 + pushY + Math.sin(particle.orbit) * 0.012;
      particle.vx *= 0.74;
      particle.vy *= 0.74;
      particle.x += particle.vx;
      particle.y += particle.vy;

      const distance = Math.hypot(dx, dy);
      const alpha = Math.max(0.16, Math.min(0.82, 1 - distance / 260));
      context.beginPath();
      context.arc(particle.x, particle.y, particle.size, 0, Math.PI * 2);
      context.fillStyle = `rgba(${particle.color}, ${alpha})`;
      context.fill();
    });

    requestAnimationFrame(draw);
  }

  section.addEventListener("pointermove", (event) => {
    const rect = section.getBoundingClientRect();
    state.pointer.x = event.clientX - rect.left;
    state.pointer.y = event.clientY - rect.top;
  });

  section.addEventListener("pointerleave", () => {
    state.pointer.x = -9999;
    state.pointer.y = -9999;
  });

  document.addEventListener("extreme:language", () => {
    state.words = getWords();
    state.wordIndex = 0;
    assignWordTargets(true);
  });

  resize();
  window.addEventListener("resize", resize);
  requestAnimationFrame(draw);
}

function initSpotlightCards() {
  const spotlightItems = document.querySelectorAll(".feature-card, .price-card, .steps article, .proof-list div, .route-card");
  if (!spotlightItems.length || reduceMotion) return;

  spotlightItems.forEach((item) => {
    item.addEventListener("pointermove", (event) => {
      const rect = item.getBoundingClientRect();
      const x = event.clientX - rect.left;
      const y = event.clientY - rect.top;
      item.style.setProperty("--mx", `${x}px`);
      item.style.setProperty("--my", `${y}px`);

      if (item.classList.contains("feature-card")) {
        const rotateY = ((x / rect.width) - 0.5) * 5;
        const rotateX = ((0.5 - y / rect.height)) * 5;
        item.style.setProperty("--tilt-x", `${rotateX.toFixed(2)}deg`);
        item.style.setProperty("--tilt-y", `${rotateY.toFixed(2)}deg`);
      }
    });

    item.addEventListener("pointerleave", () => {
      item.style.removeProperty("--tilt-x");
      item.style.removeProperty("--tilt-y");
    });
  });
}

function initMagneticButtons() {
  const buttons = document.querySelectorAll(".btn, .quick-strip a");
  if (!buttons.length || reduceMotion) return;

  buttons.forEach((button) => {
    button.addEventListener("pointermove", (event) => {
      const rect = button.getBoundingClientRect();
      const x = event.clientX - rect.left - rect.width / 2;
      const y = event.clientY - rect.top - rect.height / 2;
      button.style.setProperty("--btn-x", `${(x * 0.12).toFixed(2)}px`);
      button.style.setProperty("--btn-y", `${(y * 0.16).toFixed(2)}px`);
    });

    button.addEventListener("pointerleave", () => {
      button.style.removeProperty("--btn-x");
      button.style.removeProperty("--btn-y");
    });
  });
}

function initRevealMotion() {
  const revealItems = document.querySelectorAll(
    ".section-copy, .feature-card, .section-heading, .steps article, .pricing-copy, .price-card, .trust-media, .trust-copy, .contact-panel, .map-wrap"
  );

  if (!revealItems.length) return;
  revealItems.forEach((item) => item.classList.add("reveal-magic"));

  if (reduceMotion || !("IntersectionObserver" in window)) {
    revealItems.forEach((item) => item.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.16 }
  );

  revealItems.forEach((item) => observer.observe(item));
}

